class User < ApplicationRecord
  # before_validation :ensure_session_token
  after_initialize :ensure_session_token #both work!

  validates :username, :session_token, :password_digest, presence: true
  validates :username, :session_token, uniqueness: true
  validates :password, length: { minimum: 6, allow_nil: true }

  attr_reader :password

  def self.find_by_credentials(username, password)
    user = User.find_by(username: username)
    user && user.is_password?(password) ? user : nil
  end

  def is_password?(password)
    BCrypt::Password.new(self.password_digest).is_password?(password)
  end

  def password=(password)
    @password = password

    #why don't we need to save? Does this update the table too?
    #A: because save logic is in the controller

    #IMPORTANT! WE HAVE TO UNCOMMENT THE BCRYPT GEM IN GEMFILE IF IT'S NOT FOUND
    self.password_digest = BCrypt::Password.create(password)
  end

  def reset_session_token!
    self.session_token = SecureRandom.urlsafe_base64(16)
    self.save!
    self.session_token
  end

  private

  def ensure_session_token
    self.session_token ||= SecureRandom.urlsafe_base64(16)
  end
end
